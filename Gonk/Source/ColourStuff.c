/***************** SussOutCMap ************************/
//
//  Set ViewPort Colour Map. Given a colour map, it will
// set the viewport and projects palette.
// Inputs:
//		numofcolours = Number of Colours in list. 0 < numofcolours < 257
//		cmapok = Colour Map Ok. Set this if you are sure that the colour
//							map is 8 bit. If FALSE it will scale it to 8 bit if
//							it thinks that the colour map is currently 4 bit.
//		colours = Colour map. This can be a 4 or 8 bit colour map. In
//							UBYTE r,g,b format.
//		pal = Pointer to a palette struct. Set this only if you want the palette
//						struture set with the final result, else set it to NULL.
//		scr = Pointer to Screen. This can be NULL if you don't want
//					the screens palette to change. It will still set the projects
//					palette, if the palette parameter is valid.
// Outputs:
//		TRUE  -  Every went Ok.
//		FALSE -  Something stuffed up.

BOOL SussOutCMap(UWORD numofcolours,BOOL cmapok,UBYTE *colours,
								struct Palette *pal,struct Screen *scr)
{
	UBYTE			*rgb, r, g, b;
	BOOL			is8bit;
	struct Palette *pal8;
	struct RGB8	*ct8;
	UWORD			count;

	if( (numofcolours == 0) || (numofcolours > 256) || (colours == NULL) )
		return(FALSE);


	/* Well, first things first. We want to make up an 8 bit significant colour
		table. */
	if( !(pal8 = AllocVec(sizeof(struct Palette),MEMF_ANY|MEMF_CLEAR)) )
		return(FALSE);

	pal8->pl_Count = numofcolours;
	ct8 = pal8->pl_Colours;

	is8bit			= FALSE;
	rgb					= colours;
	for(count=0; count<numofcolours; count++) 
	{
		ct8[count].r = r = *rgb++;
		ct8[count].g = g = *rgb++;
		ct8[count].b = b = *rgb++;

		/* If any of the lower nibbles for any gun are set,
		 then we have an 8 bit CMAP, else chances are it's
		 4 bit. If cmapok=TRUE then none of this matters anyway. */

    if(((r & 0x0F) || (g & 0x0F) || (b & 0x0F)))
			is8bit = TRUE;
	}

	/* If no file/user indication that this is an 8-bit significant CMAP */
	/* then convert it to one. */
	if( (!(cmapok)) && (!(is8bit)) )
	{
		//printf("Rescaling Palette\n");
		/* If all nibbles appear shifted (4 bit), duplicate the nibbles */
		for(count = 0; count<numofcolours; count++)
		{
			ct8[count].r |= (ct8[count].r >> 4);
			ct8[count].g |= (ct8[count].g >> 4);
			ct8[count].b |= (ct8[count].b >> 4);
		}
	}

	/* if a palette structure is given, then copy our groovy 8bit significant
		colour map into the palette structure */
	if(pal)
	{
		pal->pl_Count = numofcolours;
		for(count=0; count<numofcolours; count++)
		{
			pal->pl_Colours[count].r = ct8[count].r;
			pal->pl_Colours[count].g = ct8[count].g;
			pal->pl_Colours[count].b = ct8[count].b;
		}
	}

	if(scr)
		InstallPalette(scr,pal8);

	FreeVec(pal8);

	return(TRUE);
}
